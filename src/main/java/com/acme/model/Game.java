package com.acme.model;

public class Game{
	private Integer id;
	private Integer rank;
	private String opponent;
	private Integer hokieScore;
	private Integer oppScore;
	private String winLoss;
	private String tv;
	private String location;
	private String date;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getRank() {
		return rank;
	}
	public void setRank(Integer rank) {
		this.rank = rank;
	}
	public String getOpponent() {
		return opponent;
	}
	public void setOpponent(String opponent) {
		this.opponent = opponent;
	}
	public Integer getHokieScore() {
		return hokieScore;
	}
	public void setHokieScore(Integer hokieScore) {
		this.hokieScore = hokieScore;
	}
	public Integer getOppScore() {
		return oppScore;
	}
	public void setOppScore(Integer oppScore) {
		this.oppScore = oppScore;
	}
	public String getWinLoss() {
		return winLoss;
	}
	public void setWinLoss(String winLoss) {
		this.winLoss = winLoss;
	}
	public String getTv() {
		return tv;
	}
	public void setTv(String tv) {
		this.tv = tv;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
}
